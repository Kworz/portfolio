<h3 class="mt-4 mb-2">Logiciel</h3>

<p>
    Cette machine embarque le logiciel <a href="/projects/nuster-kit">Nuster</a> que j'ai développé pour Metalizz.
    Nuster est configuré pour supporter la regulation de température, et les cycles de nettoyage.
</p>

